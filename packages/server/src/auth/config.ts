export interface AuthConfig {
  /** Whether auth is enabled */
  enabled: boolean;
  /** Human-readable access code (e.g. "blue-tiger-42") */
  accessCode: string;
  /** Secret for signing session cookies */
  sessionSecret: string;
  /** Cookie name */
  cookieName: string;
  /** Cookie max-age in milliseconds */
  cookieMaxAge: number;
  /** Max failed attempts before rate-limiting */
  maxAttempts: number;
  /** Lockout duration in milliseconds after max failed attempts */
  lockoutMs: number;
}

/** Generate a human-readable access code: adjective-noun-NN */
function generateAccessCode(): string {
  const adj = ADJECTIVES[crypto.randomInt(ADJECTIVES.length)];
  const noun = NOUNS[crypto.randomInt(NOUNS.length)];
  const num = crypto.randomInt(100).toString().padStart(2, "0");
  return `${adj}-${noun}-${num}`;
}

export function loadAuthConfig(
  overrides: { enabled?: boolean; accessCode?: string } = {},
): AuthConfig {
  const enabled = overrides.enabled ?? true;
  const accessCode = overrides.accessCode ?? generateAccessCode();

  return {
    enabled,
    accessCode,
    sessionSecret: crypto.randomBytes(32).toString("hex"),
    cookieName: "lgtm_session",
    cookieMaxAge: 24 * 60 * 60 * 1000, // 24 hours
    maxAttempts: 5,
    lockoutMs: 60 * 1000, // 1 minute
  };
}
